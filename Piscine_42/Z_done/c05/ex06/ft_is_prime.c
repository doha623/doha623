#include	<unistd.h>

int	ft_is_prime(int	nb)
{
	int	i;

	i = 2;
	if (nb < 2)
		return (0);
	while (nb / 2 >= i)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}
